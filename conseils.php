<h1>RÉUSSIR VOTRE ENTRETIEN DE STAGE</h1>
<div class="texte3">		
	Le futur stagiaire devra se préparer à son rendez-vous d’embauche comme s’il s’agissait d’un emploi. Cet entretien doit être considéré comme une première approche du monde professionnel. Nous vous proposons quelques règles simples qui vous aideront à réussir cette première étape :<br>
<br>
<span style="color:#000000;"><span style="background-color: rgb(211, 211, 211);">LA VEILLE DE L'ENTRETIEN : </span></span><br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Relisez et gardez bien en mémoire votre lettre et le CV que vous avez envoyés à l’entreprise.</span> Cela vous permettra de répondre facilement à votre interlocuteur.<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Réfléchissez à vos points forts : </span>un stage que vous avez déjà fait ou un petit job qui a un rapport avec ce qui se fait dans cette entreprise, vous aurez certainement l'occasion de le dire pendant l'entretien.<br>
<br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Renseignez-vous sur l’entreprise susceptible de vous recruter.</span> Connectez-vous sur le site web de l’entreprise, si elle en a un. Lisez le texte qui présente l’entreprise, notez quelques informations, vous pouvez aussi préparer une ou deux questions que vous pourrez poser lors de l’entretien. Montrez que vous avancez en terrain connu, l’employeur appréciera.<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Préparez votre itinéraire :</span> est-ce que c’est loin ? Repérez l’arrêt de bus, la station de tram ou de train la plus proche de l’entreprise. Y a-t-il des correspondances, des changements de bus? Mesurez le temps qu’il vous faut pour y aller.<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Entraînez-vous avec des proches</span>: Simulez des « situations pièges », apprenez à improviser. En entretien, il ne faut jamais paraître étonné ou stupéfait.<br>
<br>
<br>
<strong><span style="color:#000000;"><u><span style="background-color: rgb(211, 211, 211);">LE JOUR DE L'ENTRETIEN : </span></u></span></strong><br>
<br>
<span style="color:#ff8c00;">Comment s'habiller?</span> Bien sûr, il faut faire un petit effort de présentation pour aller à un entretien! Par votre tenue, vous montrez aussi que vous prenez cet entretien et ce futur stage au sérieux.<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Evitez de vous habiller de façon négligée…ni trop exubérante !</span> Choisissez un look sobre !<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Pour les garçons,</span> si vous avez une chemise, c’est l’occasion de la porter ! Pour les filles, si vous vous maquillez, choisissez un maquillage discret. Pendant l'entretien.<br>
<br>
<span style="color:#ff8c00;">- </span><span style="color:#000000;">Soyez souriant </span>(une mine défaite n’inspire pas confiance.)<br>
<br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Ne ratez pas votre entrée : </span>Une poignée de main ferme. Ne pas s’asseoir en premier. Regarder l’interlocuteur en face.<br>
<br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Ne soyez pas fier au point de vous croire indispensable à l’entreprise. </span>Une explication honnête et réaliste de votre profil et de vos motivations suffiront à convaincre l’employeur.<br>
<br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Pendant la discussion, évitez toute allusion politique ou religieuse, ainsi qu’il est d’usage dans toute conversation avec une personne inconnue.</span><br>
<br>
<span style="color:#ff8c00;">-</span> <span style="color:#000000;">Venez avec des questions préparées.</span> Lorsque le recruteur vous pose l’éternelle question : “Alors, vous avez des questions !?” Ne répondez jamais par la négative mais plutôt par une ou deux questions introduites par un “Oui” franc et dynamique. Vous donnerez alors l’impression d'être intéressé par le poste.<br>
<br>
<span style="color:#000000;"><u>Enfin, le maître mot :</u></span> adaptez-vous à votre interlocuteur. Vous êtes là pour vendre votre profil et lui faire savoir à quel point vous pouvez lui être utile. Ni la vente forcée, ni la vente à rebroussepoil ne fonctionneront. Et même si vous devez essuyer un échec (ce qui est rare dans le cas d’une proposition de stage), la seconde tentative sera la bonne !<br>
