            <h1>COMMENT RÉUSSIR SA LETTRE DE MOTIVATION</h1>

			<div style="width:800px;margin:0 auto;text-align:left;">		
	Une lettre de motivation pour obtenir un stage a pour principal but de démontrer l’adéquation entre les compétences du stagiaire, son projet ainsi que les objectifs de l’entreprise.<br>
<br>
<span style="color:#000000;">Pour réussir sa lettre de motivation&nbsp;:</span><br>
<br>
La lettre de motivation doit être présentée en trois parties principales.<br>
<br>
<span style="color:#ff8c00;">Premièrement,</span> vous devez commencer par vos cordonnées (Nom, prénom, adresses, cordonnées téléphoniques et adresse e-mail) en haut de la page à droite évidemment&nbsp;!<br>
<br>
<span style="color:#ff8c00;">Juste après</span> on commence par le traditionnel «Madame, Monsieur ».<br>
<br>
<span style="color:#ff8c00;">Ensuite </span>vous devez&nbsp; écrire votre demande de stage en précisant si elle est inscrite dans le cadre de vos études et le type de stage que vous voulez effectuez (opérationnel, observation, fonctionnel, PFE)<br>
<br>
<span style="color:#ff8c00;">Après</span> vous devez citer vos expériences, vos compétences, et expliquez ce qui vous attire dans ce stage en leur montrons que vous êtes vraiment intéressé par l’obtention de ce dernier qui sera une très grande opportunité pour votre carrière professionnelle, tous ceci va vous permettre de montrer au recruteur ce que vous pourriez apporter de plus à son entreprise durant votre stage. Si votre lettre est une réponse à une annonce, vous devez établir le lien entre les qualités recherchées et vos propres capacités afin de répondre à la demande du recruteur, sinon si votre candidature est spontanée insisté sur vos expériences en intégrant un mini CV et sur le choix de cette entreprise en particulier.<br>
<br>
<span style="color:#ff8c00;">Finalement,</span> la fameuse formule de politesse pour clôturer votre lettre. Ça sera parfait si vous optez pour un simple remerciement sans oublier les salutations et de préciser l’attente d’une réponse favorable, exemple&nbsp;: Veuillez agréez l’expression de mes respectueuses et sincères salutations Madame, Monsieur. En attente d’une réponse favorable. Merci pour votre intérêt.<br>
<br>
Signature à la fin.<br>

